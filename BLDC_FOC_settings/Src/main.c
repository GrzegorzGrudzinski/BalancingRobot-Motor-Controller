uint8_t calculate_checksum(void* data, size_t length) {
    uint8_t checksum = 0;
    uint8_t* byte = (uint8_t*)data;

    for (size_t i = 0; i < length-1; i++) {
        checksum ^= byte[i];
    }

    return checksum;
}


void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if (huart->Instance == USART2)
  {
    static uint8_t rxState_flag = 0; // 0 -looking for start byte, 1 - receiving data

    if (rxState_flag == 0) {
      if (rxRawBuffer[0] == 0xAA) { // Start byte found
        rxState_flag = 1;

        HAL_UART_Receive_IT(huart, &rxRawBuffer[1], sizeof(MotorPacket_t) - 1); // Receive rest of the packet
      }
    }
    else if (rxState_flag == 1) {
      // Full packet received
      MotorPacket_t* recieved_pkt = (MotorPacket_t*)rxRawBuffer;

      //calcuate checksum and verify
      uint8_t checksum = calculate_checksum(rxRawBuffer, sizeof(MotorPacket_t));
      if (recieved_pkt->checksum == checksum) {
        // Valid packet
        motor_data = *recieved_pkt;
        rx_msg_recieved = 1;
      }

      rxState_flag = 0; // Reset state to look for next packet
      // Restart reception for next packet
      HAL_UART_Receive_IT(huart, rxRawBuffer, 1);
    } 
  } // if USART2
}


void send_feedback(UART_HandleTypeDef *huart) {
    static FeedbackPacket_t packet ;
    
    packet.startByte = 0xBB;
    packet.status = MC_GetSTMStateMotor1();
    packet.speed_rpm = MC_GetAverageMecSpeedMotor1_F();
    packet.current_Iq = MC_GetIqdMotor1_F().q;
    packet.current_Id = MC_GetIqdMotor1_F().d;
    packet.checksum = calculate_checksum(&packet, sizeof(FeedbackPacket_t));
    
    // Only send if UART is ready
    uint8_t state = HAL_UART_GetState(huart);
    if ( (state == HAL_UART_STATE_READY || state == HAL_UART_STATE_BUSY_RX) ) {
         HAL_UART_Transmit_DMA(huart, (uint8_t*)&packet, sizeof(FeedbackPacket_t));
    }
}

void send_init(UART_HandleTypeDef *huart) {
    static FeedbackPacket_t packet ;
    
    packet.startByte = 0xBB;
    packet.status = (uint32_t)ESC_HELLO;
    packet.speed_rpm = MC_GetAverageMecSpeedMotor1_F();
    packet.current_Iq = MC_GetIqdMotor1_F().q;
    packet.current_Id = MC_GetIqdMotor1_F().d;
    packet.checksum = calculate_checksum(&packet, sizeof(FeedbackPacket_t));
    
    // Only send if UART is ready
    uint8_t state = HAL_UART_GetState(huart);
    if ( (state == HAL_UART_STATE_READY || state == HAL_UART_STATE_BUSY_RX) ) {
         HAL_UART_Transmit_DMA(huart, (uint8_t*)&packet, sizeof(FeedbackPacket_t));
    }
}

